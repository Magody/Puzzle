    private BufferedImage cambiarTam(int tam){ 
        ImageIcon icon = new ImageIcon(imagenBuffer);
        Graphics2D g2d = (Graphics2D) imagenBuffer.createGraphics(); 
        g2d.addRenderingHints(new RenderingHints(RenderingHints.KEY_RENDERING,RenderingHints.VALUE_RENDER_QUALITY)); 
        g2d.drawImage(icon.getImage(), 0, 0, tam, tam, null); 
        return imagenBuffer; 
    }
    
