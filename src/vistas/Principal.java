    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        dir = new File("C:/Users/Usuario/Desktop");
        JFileChooser selectorImg = new JFileChooser();
        selectorImg.setCurrentDirectory(dir);
        selectorImg.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagenes jpg, png", "jpg", "png");
        selectorImg.setFileFilter(filtro);
        if(selectorImg.showOpenDialog(this)==0){
            File archivo = selectorImg.getSelectedFile();
            if ((archivo == null) || (archivo.getName().equals(""))){
                JOptionPane.showMessageDialog(this, "Nombre de archivo inválido", "Nombre de archivo inválido", JOptionPane.ERROR_MESSAGE);
            }else{
                jLNombre.setText(archivo.getName());
                try {
                    jPImagen.removeAll();
                    imagenBuffer = ImageIO.read(archivo);
                    ImageFondo fondoClass = new ImageFondo();
                    fondoClass.setImage(imagenBuffer);
                    jPImagen.add(fondoClass);
                    jPImagen.revalidate();
                    jPImagen.repaint();
                    fileLoad = true;
                } catch (IOException ex) {
                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

